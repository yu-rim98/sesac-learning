// if 조건문의 기본 구조

/*
  if (조건식) { 참 일 때 실행 }
*/

if (5 > 1) {
  console.log("5는 1보다 큼");
}

/* if ~ else if 조건문 기본 구조
  if (조건식) {
    참 일 때 실행
  } else if (조건식) { // if 조건식이 거짓일 때
    참 일 때 실행
  }
*/

if (5 > 10) {
  console.log("5는 10보다 큼");
} else if (5 > 7) {
  console.log("5는 7보다 큼");
} else if (5 > 5) {
  console.log("5는 5보다 큼");
} else if (5 > 3) {
  console.log("5는 3보다 큼");
}
